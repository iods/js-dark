// A Variable is a symbolic value for a value, a data-container.

// variables are declared with the let keyword
let x;               // declared, not assigned

x = 0;               // assign the variable x a value
x                    // a variable will always evaluate to it's value (??)

// JavaScript supports several types of values for variables
x = 1;               // numbers
x = 0.01;            // numbers can be both integers or reals
x = "Hello, World";  // strings of text in quotation marks
x = 'JavaScript';    // single quote marks also delimit strings
x = true;            // represent a boolean value
x = false;           // represents the other boolean value xD
x = null;            // null is a special value that represents no value
x = undefined;       // undefined is another special value like null